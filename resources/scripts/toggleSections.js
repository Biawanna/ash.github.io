const videosButton = document.getElementById("videos-button");
const musicButton = document.getElementById("music-button");
const projectsButton = document.getElementById("projects-button");

const videoSection = document.getElementById("video-section");
const musicSection = document.getElementById("spotify-widget");
const projectsSection = document.getElementById("personal-projects");

function hideAllSections() {
    videoSection.style.display = "none";
    musicSection.style.display = "none";
    projectsSection.style.display = "none";
}

videosButton.addEventListener("click", () => {
    hideAllSections();
    videoSection.style.display = "flex";
});

musicButton.addEventListener("click", () => {
    hideAllSections();
    musicSection.style.display = "flex";
});

projectsButton.addEventListener("click", () => {
    hideAllSections();
    projectsSection.style.display = "flex";
});

hideAllSections();
